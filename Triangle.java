
public class Triangle {

	private int height;
	private int width;
	
	//Returns Triangle's Height
	public int getHeight() {
		return height;
	}

	//Returns Triangle's Width
	public int getWidth() {
		return width;
	}

	//Return's the area of the Triangle
	public double getArea() {
		return 0.5*width*height;
	}
}
